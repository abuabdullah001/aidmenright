    public function store(Request $request)
    {
        // $donation->donation_type='';

        $user = auth()->user();

        // Create a new Donation instance
        $donation = new Order();


        $donation->name = $request->input('name');
        $donation->amount = $request->input('amount');
        $donation->email = $request->input('amount');
        $donation->phone = $request->input('phone');
        $donation->address = $request->input('address');
        $donation->status = $request->input('status');
        $donation->transaction_id = $request->input('transaction_id');
        $donation->currency = $request->input('currency');

        $donation->donation_type = ($user->type == 1) ? 'custom' : 'regular';

        $donation->save();



        return redirect('/donate/list')->with('success', 'Donation added successfully!');
    }

    public function success(Request $request)
    {

        echo "<h1 style='color: green;'>Transaction is Successful</h1>";

        $tran_id = $request->input('tran_id');
        $amount = $request->input('amount');
        $currency = $request->input('currency');

        $sslc = new SslCommerzNotification();

        # Check order status in order table against the transaction id or order id.
        $order_details = Order::
            where('transaction_id', $tran_id)
            ->select('transaction_id', 'status', 'currency', 'amount')->first();

        if ($order_details->status == 'Pending') {
            $validation = $sslc->orderValidate($request->all(), $tran_id, $amount, $currency);

            if ($validation) {
                # Update order status to Processing.
                $update_product = Order::
                    where('transaction_id', $tran_id)
                    ->update(['status' => 'Processing']);

                echo "<div style='text-align: center; padding: 20px; background-color: #d4edda; color: #155724;'>
                        <h2>Transaction is successfully completed!</h2>
                        <p>Your transaction ID is: <strong>$tran_id</strong></p>
                        <p>Amount: <strong>$amount $currency</strong></p>
                        <a href='/order-details' style='padding: 10px; background-color: #28a745; color: white; text-decoration: none;'>View Order</a>
                      </div>";
            }
        } else if ($order_details->status == 'Processing' || $order_details->status == 'Complete') {
            echo "<div style='text-align: center; padding: 20px; background-color: #cce5ff; color: #004085;'>
                    <h2>Transaction is already completed!</h2>
                    <p>Your transaction ID is: <strong>$tran_id</strong></p>
                    <p>Amount: <strong>$amount $currency</strong></p>
                  </div>";
        } else {
            echo "<div style='text-align: center; padding: 20px; background-color: #f8d7da; color: #721c24;'>
                    <h2>Invalid Transaction</h2>
                    <p>Something went wrong. Please try again.</p>
                  </div>";
        }
    }

